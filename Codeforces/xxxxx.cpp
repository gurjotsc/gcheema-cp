ll modPow(ll a, ll b, ll m) {
    a %= m;
    ll res = 1;
    while (b > 0) {
        if (b & 1)
            res = res * a % m;
        a = a * a % m;
        b >>= 1;
    }
    return res;
}


ll solve() {
    ll n, x; cin >> n >> x;
	vector<ll> a(n); cin >> a;
    ll l = -1, r = 0;
	ll sm = 0ll;
	//the first and last elements that aren't divisible by x will create the longest
	//subarrays where the sum isn't divisble by x. For instance, removing a number
	//not divisible by x from a sum that IS divisible x, will then make the sum not
	//divisible by x.
	for(int i = 0; i < n; ++i) {
		if(a[i]%x){
			l = (l == -1) ? i : l; //idx of first element not divisible by x
			r = i; //will eventually sore last element not divisible by x
		}
		sm += a[i];
	}
	if(sm%x) return n; //whole array sum isn't divisible by x
	if(l == -1) return -1; //there is no element that isn't divisble by x
	
	//remove either the prefix or suffix that
	// will make the sum not divisible by x
	return n-(min(l+1, n-r));

}


